Yu Kanda is a Second Exorcist, an artificial body possessing the brain of an Exorcist who died thirty years before the storyline. The Black Order launched the Synthetic Disciple Project nine years before the storyline, hoping that Second Exorcists would be able to synchronize with their original Innocence. Because of the project, he is physically 19 years old but has lived for only nine years. As a side effect, he sees lotus flowers blooming wherever he goes; when there was one flower, he was told that it was an illusion, and since then the flowers have multiplied to cover everything.[ch. 186] Kanda was voiced by Takahiro Sakurai[4] and will be voiced by Takuya Sato in D.Gray-man Hallow in Japanese. He is voiced by Travis Willingham in English.ep. 2
