                Lenalee Lee<br>
Lenalee Lee is a 16-year-old Exorcist from China.[vol. 1:172] Her parents were killed by an Akuma when she was very young, and she was forced to become an Exorcist as a child. Because she was separated from her brother Komui, her only remaining family member, she repeatedly tried to escape and was restrained. This caused her to hate the Order and Innocence. When Komui joined the Order to be with her, she stopped trying to escape. She now fights for her brother as well as her friends, as she now knows she has a home and family to return to.[ch. 21] Her perception of the "world" consists of her friends and family; whenever one of her friends dies, it seems to her as if a part of her world has been destroyed.[ch. 69]

The Dark Boots, her Equipment-type anti-Akuma weapon, take the form of a pair of black, knee-high boots that give her the ability to run at high speeds, jump to great heights, walk on water, create whirlwinds and use sound waves as footholds.[ch. 18,48,67] To defeat a Level 3 Akuma, she evoked more power out of her Innocence than she could safely handle, reducing her ability to use her boots as well as her physical mobility. During this state, her Innocence would become a crystal to protect her from harm, a behavior unheard of in Innocence.[ch. 71] This caused the Earl to believe that Lenalee might possess the Great Heart. After, when a Level 4 Akuma attacks the HQ, and many people were injured or killed, she expresses a desire to willingly become an Exorcist. Accepting her conviction, the Innocence grants her greater power, becoming a never-before-seen evolved Equipment-type, dubbed a Crystal-type. These new Boots are made up of Lenalee's blood and will repair themselves as long as she has blood in her body. When the Dark Boots are in their dormant state, they form two blood-red anklets.[ch. 157] When Kanda and Johnny ran away, she and Marie keep their location a secret from Komui. Lenalee was voiced by Shizuka Itō and will be voiced by Ai Kakuma in D.Gray-man Hallow in Japanese. She is voiced by Luci Christian in English.ep. 2
