Lavi<br> 
Lavi  is a cheerful 18-year-old red-haired Exorcist of mixed heritage. He aims to become a Bookman, a person who records the hidden history of the world, and has been trained from a young age to achieve that goal.[6] Lavi is the 49th alias he assumed after casting away his real name to reach that goal.He works alongside the Black Order just to be close to the events that must be seen, but slowly becomes more attached to his Black Order friends, and his original Bookman self is constantly being clouded by his growing care for them.In recent chapters, he and Bookman were kidnapped by the Clan of Noah and parasites from the Noah were put into Lavi in an attempt to get Bookman to give them information about the 14th Noah. Hoshino was surprised by Lavi's popularity as he made it to the top ten of latest character poll despite his lack of appearances in later story arcs.[9]

His anti-Akuma weapon, simply named Tettsuiand referred to as Ōzuchi Kozuchi, is capable of growing and extending to undefined sizes and lengths.[6][10][11] Although this affects its weight, it seems to not affect Lavi.[12] The weapon allows him to use "Seals" that control several elements of nature.Lavi was voiced by Kenichi Suzumuraand will be voiced by Natsuki Hanae in D.Gray-man Hallow in Japanese. In English, he is voiced by Jason Liebrecht from episodes 13 to 26 and episode 40 onwards, and by Chris Patton in episodes 27 to 39.
