                    Allen Walker<br>

                   Allen Walker is a warm-hearted 15-year-old British Exorcist and the main protagonist of the series.[vol. 1:62] He turned his adoptive father, Mana Walker, into an Akuma (demon), and during this incident, Allen's left eye was cursed by his father, granting him the ability to see the souls of Akuma, and his left arm awoke as an anti-Akuma weapon.[ch. 3] Allen is revealed to be the 14th Noah, Nea, and will eventually be taken over by Nea's consciousness and become a Noah.[ch. 136,166] As a result, Allen is placed under suspicion and eventually confined.[ch. 202] He is freed by the Noah, causing the Order to revoke his status as an Exorcist and treat him as a Noah, despite the fact he refuses to join the Earl's side.[ch. 204] He vows to refuse help from both the Order and the Noah, but he still considers himself an Exorcist. At the end of Chapter 214, it is shown that he is overtaken by the 14th who had been living in his body for years.[ch. 205]

                   Allen's Innocence starts out as a large grey claw, but during his fight with a Level Two Akuma, his arm begins to evolve and may take on the shape of a large gun when activated.[1] Later on, Allen's Innocence is nearly destroyed by Tyki, but in the process Allen loses his arm.[2] After struggling to reform his Innocence (which has been turned into dust), a Level Three Akuma attacks the Asian Branch where he is recovering. During the fight, Allen realizes that he is fighting for both his friends and the Akuma, and his Innocence is able to realize its true form, the Crown Clown. The Parasite-type anti-Akuma weapon, Crown Clown .[ch. 85], takes the form of a claw on his left hand and a white, hooded, fur cowl. Allen is also able to transform his left arm into a large sword, which resembles The Millennium Earl's but with inverted colors. He can exorcise evil from anything the sword strikes.[ch. 116,117] Allen was voiced by Sanae Kobayashiep. 1 and will be voiced by Ayumu Murase in D.Gray-man Hallow in Japanese.He is voiced by Todd Haberkorn in English.ep. 1
